    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:

        DefaultTableModel d1 = (DefaultTableModel) jTable1.getModel();
        int SelectIndex = jTable1.getSelectedRow();

        int id = Integer.parseInt(d1.getValueAt(SelectIndex, 0).toString());
        String product = txtProduct.getText();
        String desc = txtDescription.getText();
        CategoryItem citem = (CategoryItem) txtCategory.getSelectedItem();
        BrandItem bitem = (BrandItem) txtBrand.getSelectedItem();
        String cost = txtCostPrice.getText();
        String retail = txtRetailPrice.getText();
        String qty = txtQty.getText();
        String barcode = txtBarCode.getText();
        String status = txtStatus.getSelectedItem().toString();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarket", "root", "");

            pst = conn.prepareStatement("UPDATE product SET product =?, description = ?, cat_id =?, brand_id = ?, cost_price =?, retail_price = ? ,qty =?, barcode = ?, status =?  WHERE id=?");
            pst.setString(1, product);
            pst.setString(2, desc);
            pst.setInt(3, citem.id);
            pst.setInt(4, bitem.id);
            pst.setString(5, cost);
            pst.setString(6, retail);
            pst.setString(7, qty);
            pst.setString(8, barcode);
            pst.setString(9, status);
            pst.setInt(10, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Product updated");
            table_update();

            //to clear the data
            txtProduct.setText("");
            txtDescription.setText("");
            txtCategory.setSelectedIndex(-1);
            txtBrand.setSelectedIndex(-1);
            txtCostPrice.setText("");
            txtRetailPrice.setText("");
            txtQty.setText("");
            txtBarCode.setText("");
            txtStatus.setSelectedIndex(-1);
            txtProduct.requestFocus();

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_updateBtnActionPerformed

