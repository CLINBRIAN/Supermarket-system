    private void table_update() {
        try {
            int c;
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarket", "root", "");
                //union of three tables in the database
                pst = conn.prepareStatement("SELECT p.id,p.product,p.description, c.category, b.brand, p.cost_price, p.retail_price,p.qty,p.barcode,p.status FROM product p, category c, brand b WHERE p.cat_id = c.id and p.brand_id = b.id ");
                rs = pst.executeQuery();

                rm = rs.getMetaData();
                c = rm.getColumnCount();

                DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
                d.setRowCount(0);

                while (rs.next()) {
                    Vector v = new Vector();

                    for (int i = 0; i <= c; i++) {
                        v.add(rs.getString("id"));
                        v.add(rs.getString("product"));
                        v.add(rs.getString("description"));
                        v.add(rs.getString("category"));
                        v.add(rs.getString("brand"));
                        v.add(rs.getString("cost_price"));
                        v.add(rs.getString("retail_price"));
                        v.add(rs.getString("qty"));
                        v.add(rs.getString("barcode"));
                        v.add(rs.getString("status"));
                    }
                    d.addRow(v);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
